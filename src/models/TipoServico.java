package models;

/**
 *
 * @author Glauber
 */
public class TipoServico {

    private int id;
    private String nome;
    private float valor;

    public TipoServico(String nome, float valor) {
        this.nome = nome;
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    @Override
    public String toString() {
        return String.format("%s - R$ %.2f", nome, valor);
    }
    
}
